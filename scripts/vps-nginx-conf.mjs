/** Shared nginx config for battle worker WebSocket routes. */
export const WORKER_BASE_PORT = 8101;

/**
 * @param {number} workerCount
 * @param {number} [workerBasePort]
 * @param {{ monitoring?: boolean, publicHost?: string }} [opts]
 */
export function buildBattleNginxConf(workerCount, workerBasePort = WORKER_BASE_PORT, opts = {}) {
  const monitoring = opts.monitoring !== false;
  const publicHost = opts.publicHost || "217.60.245.116";
  let wblocks = "";
  for (let i = 1; i <= workerCount; i++) {
    const port = workerBasePort + i - 1;
    wblocks += `
    location /w${i}/ {
        proxy_pass http://127.0.0.1:${port}/;
        proxy_http_version 1.1;
        proxy_set_header Upgrade $http_upgrade;
        proxy_set_header Connection "upgrade";
        proxy_set_header Host $host;
        proxy_set_header X-Real-IP $remote_addr;
        proxy_read_timeout 86400;
    }
`;
  }
  const monitoringBlock = monitoring
    ? `
    location /grafana/ {
        auth_basic "Starfall Metrics";
        auth_basic_user_file /etc/nginx/.htpasswd-starfall;
        proxy_pass http://127.0.0.1:3000/;
        proxy_http_version 1.1;
        proxy_set_header Host $host;
        proxy_set_header X-Real-IP $remote_addr;
        proxy_set_header X-Forwarded-For $proxy_add_x_forwarded_for;
        proxy_set_header X-Forwarded-Proto $scheme;
        proxy_read_timeout 300;
    }

    location /prometheus/ {
        auth_basic "Starfall Metrics";
        auth_basic_user_file /etc/nginx/.htpasswd-starfall;
        proxy_pass http://127.0.0.1:9090/;
        proxy_http_version 1.1;
        proxy_set_header Host $host;
        proxy_set_header X-Real-IP $remote_addr;
        proxy_read_timeout 120;
    }
`
    : "";
  return `# Generated by scripts/vps-nginx-conf.mjs — do not edit by hand.
# Grafana: http://${publicHost}/grafana/  Prometheus: http://${publicHost}/prometheus/
server {
    listen 80 default_server;
    listen [::]:80 default_server;
    server_name _;

    location /mm/ {
        proxy_pass http://127.0.0.1:8090;
        proxy_http_version 1.1;
        proxy_set_header Host $host;
        proxy_set_header X-Real-IP $remote_addr;
    }

${wblocks}${monitoringBlock}
    location /cfg/ {
        proxy_pass http://127.0.0.1:8095/;
        proxy_http_version 1.1;
        proxy_set_header Upgrade $http_upgrade;
        proxy_set_header Connection "upgrade";
        proxy_set_header Host $host;
        proxy_set_header X-Real-IP $remote_addr;
        proxy_set_header X-Forwarded-For $proxy_add_x_forwarded_for;
        proxy_read_timeout 86400;
    }

    location /cdn/ {
        proxy_pass http://127.0.0.1:8081;
        proxy_http_version 1.1;
        proxy_set_header Host $host;
        proxy_set_header X-Real-IP $remote_addr;
        proxy_read_timeout 120;
        proxy_buffering on;
    }

    location /ws/battle {
        proxy_pass http://127.0.0.1:8081;
        proxy_http_version 1.1;
        proxy_set_header Upgrade $http_upgrade;
        proxy_set_header Connection "upgrade";
        proxy_set_header Host $host;
        proxy_set_header X-Real-IP $remote_addr;
        proxy_read_timeout 86400;
    }

    location / {
        proxy_pass http://127.0.0.1:8080;
        proxy_http_version 1.1;
        proxy_set_header Upgrade $http_upgrade;
        proxy_set_header Connection "upgrade";
        proxy_set_header Host $host;
        proxy_set_header X-Real-IP $remote_addr;
        proxy_read_timeout 86400;
    }
}
`;
}
