function buildPool(): string[] {
  const out = new Set<string>(CORE);
  for (let i = 0; i < ACTIONS.length; i++) {
    const emoji = OPENERS[i % OPENERS.length]!;
    out.add(`${emoji} ${ACTIONS[i]!}`);
  }
  return [...out];
}

const POOL = buildPool();

export function pickExpandedAstralTip(): string {
  return POOL[Math.floor(Math.random() * POOL.length)]!;
}

export function getAstralTipPoolSize(): number {
  return POOL.length;
}
